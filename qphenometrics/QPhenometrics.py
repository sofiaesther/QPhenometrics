    def SOSFinder(self,YD):
        SOSfind = 0
        counterreg = 0
        counter = 0
        for i in range(len(YD)):
            if counter == 0:
                SOScounter = i
            if i < len(YD) - 2 and i > 0:
                if YD[i + 1] > 0 or YD[i + 1] > YD[i - 1]:
                    counter = counter + 1
                else:
                    if counter > counterreg:
                        counterreg = counter
                        counter = 0
                        SOSfind = SOScounter

            SOS = int(SOSfind)

        return SOS

    def EOSFinder(self,YD):
        EOSfind = 0
        counter = 0
        for i in range(len(YD)):
            if i > 0 and i < len(YD) - 1:
                if YD[i - 1] < 0 or YD[i - 2] > YD[i]:
                    counter = counter + 1
                    if counter > 0 and YD[i + 1] > YD[i]:
                        counter = 0
                        EOSfind = i + 1

            EOS = int(EOSfind)

        return EOS