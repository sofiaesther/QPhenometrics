        if self.dlg.cbDMA5.isChecked() == True:
            dxdy = np.diff(np.array(yDMA5[4:])) / np.diff(np.array(x_new[4:]))
            indexEOS = self.EOSFinder(dxdy)+4
            indexSOS = self.SOSFinder(dxdy)+4
            AMP = MaxValueY - yDMA5[indexSOS]
            DUR = x_new[indexEOS]-x_new[indexSOS]
            name.append('DMA5')
            MetricsDMA5.append(round(AMP,5))
            MetricsDMA5.append(DUR)
            MetricsDMA5.append(DATA2[indexSOS])
            MetricsDMA5.append(DATA2[indexEOS])
            legendlist.append(MetricsDMA5)
            colors.append('Steelblue')
            plt.plot_date(DATA2, yDMA5, '-', color='Steelblue')

        if self.dlg.cbEnsemble.isChecked() == True:

            dxdy = np.diff(np.array(yEnsemble)) / np.diff(np.array(x_new))
            indexEOS=self.EOSFinder(dxdy)
            indexSOS=self.SOSFinder(dxdy)

            AMP = MaxValueY - yEnsemble[indexSOS]
            DUR = x_new[indexEOS]-x_new[indexSOS]
